#include "../inc/cub3d.h"

void	print_map(t_map *map)
{
	int	y;

	y = 0;
	printf("=== MAP ===\n");
	while (y < map->height)
	{
		printf("%s\n", map->map[y]);
		y++;
	}
	printf("===========\n");
}
